#!/usr/bin/env python3
from __future__ import annotations

import json
import re
from collections import Counter, defaultdict
from copy import deepcopy
from pathlib import Path
from typing import Any


BASE_DIR = Path(__file__).resolve().parents[1]
QA_DIR = BASE_DIR / "QA"
LOW_RESOURCE_DIR = BASE_DIR / "用例生成" / "generated_low_resource_cases"
QA_REWRITE_PATH = BASE_DIR / "用例生成" / "generated_qa_rewrites" / "qa_local_model_rewrites.json"
JUDGE_DIR = BASE_DIR / "jailbreak" / "Qwen2.5-7B"
TRANSLATION_CACHE_PATH = (
    BASE_DIR
    / "专家审核文件生成"
    / "generated_expert_review_docs"
    / "simplified"
    / "ra_dri_translation_cache.json"
)
OUTPUT_DIR = BASE_DIR / "generated_reinforced_qa_eval_cases"

COUNTRIES = [
    {
        "country": "沙特",
        "country_dir": "Saudi",
        "prefix": "Saudi",
        "language": "arabic",
    },
    {
        "country": "泰国",
        "country_dir": "Thailand",
        "prefix": "Thailand",
        "language": "thai",
    },
    {
        "country": "土耳其",
        "country_dir": "Turkey",
        "prefix": "Turkey",
        "language": "turkish",
    },
]

QUESTION_TYPES = [
    {"label": "知识问答", "kind": "mcq", "file_stem": "MCQ_知识问答"},
    {"label": "价值贬损", "kind": "qa", "file_stem": "QA_价值贬损"},
    {"label": "违规协助", "kind": "qa", "file_stem": "QA_违规协助"},
]

RA_DRI_BUCKETS = {"value_derogation", "violation_assistance"}


def main() -> int:
    selected = collect_expert_review_selected_sources()
    summary: dict[str, Any] = {
        "output_dir": str(OUTPUT_DIR.relative_to(BASE_DIR)),
        "deduplication_policy": (
            "Regenerate from QA source files and remove QA cases selected into the third-stage expert "
            "review documents. Sources are traced from low-resource dialect examples, QA qwen rewrite "
            "examples, and cached QA RA-DRI sentence-translation examples generated by "
            "专家审核文件生成/build_expert_review_txt_files.py."
        ),
        "deduplication_key": ["source_file", "source_idx"],
        "expert_review_source_dir": "专家审核文件生成/generated_expert_review_docs/第三阶段-专家审核文件",
        "selected_source_summary": summarize_selected_sources(selected),
        "countries": {},
    }

    for country in COUNTRIES:
        country_summary = {
            "country": country["country"],
            "language": country["language"],
            "output_dir": str((OUTPUT_DIR / country["country_dir"]).relative_to(BASE_DIR)),
            "types": {},
        }
        for question_type in QUESTION_TYPES:
            source_rel = f"QA/{country['prefix']}_{question_type['file_stem']}.json"
            output_rel = f"generated_reinforced_qa_eval_cases/{country['country_dir']}/{country['prefix']}_{question_type['file_stem']}.json"
            source_path = BASE_DIR / source_rel
            output_path = BASE_DIR / output_rel
            used = selected.get(source_rel, {})
            if question_type["kind"] == "mcq":
                source_data = read_json(source_path)
                filtered_data, source_count, output_count, missing = filter_mcq(source_data, set(used))
            else:
                source_data = read_json(source_path)
                filtered_data, source_count, output_count, missing = filter_qa_list(source_data, set(used))
            if missing:
                raise ValueError(f"{source_rel} missing selected source_idx values: {missing[:10]}")
            write_json(output_path, filtered_data)
            country_summary["types"][question_type["label"]] = {
                "source_file": source_rel,
                "output_file": output_rel,
                "source_records": source_count,
                "removed_expert_review_selected": len(used),
                "removed_by_origin": dict(sorted(Counter(origin for origins in used.values() for origin in origins).items())),
                "output_records": output_count,
            }
        summary["countries"][country["country_dir"]] = country_summary

    write_json(OUTPUT_DIR / "summary.json", summary)
    print(json.dumps(summary, ensure_ascii=False, indent=2))
    return 0


def collect_expert_review_selected_sources() -> dict[str, dict[str, set[str]]]:
    selected: dict[str, dict[str, set[str]]] = defaultdict(lambda: defaultdict(set))
    collect_dialect_sources(selected)
    collect_qwen_rewrite_sources(selected)
    collect_ra_dri_translation_sources(selected)
    return selected


def collect_dialect_sources(selected: dict[str, dict[str, set[str]]]) -> None:
    for country in COUNTRIES:
        path = LOW_RESOURCE_DIR / f"{country['language']}_safety_dialect_generalization.json"
        for item in read_json_list(path):
            add_source(selected, item, "dialect_review")


def collect_qwen_rewrite_sources(selected: dict[str, dict[str, set[str]]]) -> None:
    for item in read_json_list(QA_REWRITE_PATH):
        add_source(selected, item, "rewrite_review")


def collect_ra_dri_translation_sources(selected: dict[str, dict[str, set[str]]]) -> None:
    cache = read_json(TRANSLATION_CACHE_PATH)
    if not isinstance(cache, dict):
        raise ValueError(f"{TRANSLATION_CACHE_PATH} must contain a JSON object")

    for item in sorted(read_json_list(JUDGE_DIR / "QA_judge.json"), key=judge_order):
        if str(item.get("score")).strip() != "1":
            continue
        if clean_text(item.get("attack_method")) != "RA-DRI":
            continue
        if clean_text(item.get("task_type_key")) not in RA_DRI_BUCKETS:
            continue
        language = clean_text(item.get("original_language") or item.get("dispatch_source_language"))
        sentence = first_user_message(str(item.get("malicious_query") or ""))
        if not is_reviewable_ra_prompt(sentence):
            continue
        if f"{language}\t{sentence}" not in cache:
            continue
        add_source(selected, item, "sentence_translation_ra_dri_review")


def add_source(selected: dict[str, dict[str, set[str]]], item: dict[str, Any], origin: str) -> None:
    source_file = clean_text(item.get("source_file"))
    source_idx = clean_text(item.get("source_idx"))
    if not source_file.startswith("QA/") or not source_idx:
        return
    selected[source_file][source_idx].add(origin)


def filter_qa_list(data: Any, used: set[str]) -> tuple[list[dict[str, Any]], int, int, list[str]]:
    if not isinstance(data, list):
        raise ValueError("QA source must contain a JSON list")
    source_indices = {clean_text(item.get("idx")) or str(index) for index, item in enumerate(data, start=1) if isinstance(item, dict)}
    missing = sorted(used - source_indices, key=source_idx_sort_key)
    output = [
        item
        for index, item in enumerate(data, start=1)
        if not isinstance(item, dict) or (clean_text(item.get("idx")) or str(index)) not in used
    ]
    return output, len(data), len(output), missing


def filter_mcq(data: Any, used: set[str]) -> tuple[dict[str, Any], int, int, list[str]]:
    if not isinstance(data, dict):
        raise ValueError("MCQ source must contain a JSON object")
    output = deepcopy(data)
    source_indices: set[str] = set()
    source_count = 0
    output_count = 0
    for regulation_index, regulation in enumerate(output.get("regulations", []), start=1):
        for item_index, item in enumerate(regulation.get("items", []), start=1):
            qa_list = item.get("qa", {}).get("qas", [])
            if not isinstance(qa_list, list):
                continue
            filtered = []
            for qa_index, qa in enumerate(qa_list, start=1):
                source_idx = f"reg{regulation_index}_item{item_index}_qa{qa_index}"
                source_indices.add(source_idx)
                source_count += 1
                if source_idx in used:
                    continue
                filtered.append(qa)
            item.setdefault("qa", {})["qas"] = filtered
            output_count += len(filtered)
    missing = sorted(used - source_indices, key=source_idx_sort_key)
    return output, source_count, output_count, missing


def summarize_selected_sources(selected: dict[str, dict[str, set[str]]]) -> dict[str, Any]:
    summary = {}
    for source_file in sorted(selected):
        origins = Counter(origin for origin_set in selected[source_file].values() for origin in origin_set)
        summary[source_file] = {
            "unique_source_idx": len(selected[source_file]),
            "by_origin": dict(sorted(origins.items())),
        }
    return summary


def split_rendered_messages(text: str) -> list[tuple[str, str]]:
    matches = list(re.finditer(r"(?m)^\[(system|user|assistant)\]\n", text or ""))
    if not matches:
        return [("user", clean_text(text))] if clean_text(text) else []
    segments: list[tuple[str, str]] = []
    for index, match in enumerate(matches):
        role = match.group(1)
        start = match.end()
        end = matches[index + 1].start() if index + 1 < len(matches) else len(text)
        content = clean_text(text[start:end])
        if content:
            segments.append((role, content))
    return segments


def first_user_message(text: str) -> str:
    for role, content in split_rendered_messages(text):
        if role == "user":
            cleaned = clean_text(content)
            if "未经处理的翻译结果：" in cleaned:
                cleaned = cleaned.split("未经处理的翻译结果：", 1)[1]
            if "未处理的翻译结果：" in cleaned:
                cleaned = cleaned.split("未处理的翻译结果：", 1)[1]
            return normalize_review_sentence(cleaned)
    return ""


def normalize_review_sentence(text: str) -> str:
    text = re.sub(r"\s+", " ", clean_text(text))
    text = re.sub(r"^\s*[-*•]\s*", "", text)
    text = re.sub(r"^\s*\d+[.)]\s*", "", text)
    return text.strip(" \"'")


def is_reviewable_ra_prompt(text: str) -> bool:
    if not text or len(text) < 20 or len(text) > 1200:
        return False
    if "[user]" in text or "[assistant]" in text or "[system]" in text:
        return False
    words = re.findall(r"[A-Za-z][A-Za-z'-]*", text)
    return len(words) >= 5


def judge_order(item: dict[str, Any]) -> int:
    try:
        return int(item.get("id"))
    except (TypeError, ValueError):
        return 10**12


def source_idx_sort_key(value: str) -> tuple[int, int, int, int, str]:
    mcq = re.fullmatch(r"reg(\d+)_item(\d+)_qa(\d+)", value)
    if mcq:
        return (0, int(mcq.group(1)), int(mcq.group(2)), int(mcq.group(3)), value)
    if value.isdigit():
        return (1, int(value), 0, 0, value)
    return (2, 0, 0, 0, value)


def clean_text(value: Any) -> str:
    return re.sub(r"\s+", " ", str(value or "")).strip()


def read_json(path: Path) -> Any:
    with path.open("r", encoding="utf-8") as handle:
        return json.load(handle)


def read_json_list(path: Path) -> list[dict[str, Any]]:
    data = read_json(path)
    if not isinstance(data, list):
        raise ValueError(f"{path} must contain a JSON list")
    return [item for item in data if isinstance(item, dict)]


def write_json(path: Path, data: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


if __name__ == "__main__":
    raise SystemExit(main())
